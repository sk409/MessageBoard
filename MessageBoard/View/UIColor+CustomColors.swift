import UIKit

extension UIColor {
    
    static var aqua: UIColor {
        get {
            return UIColor(red: 0, green: 0.58, blue: 1, alpha: 1)
        }
    }
    
    static var seaFoam: UIColor {
        get {
            return UIColor(red: 0, green: 1, blue: 0.57, alpha: 1)
        }
    }
    
    static var moss: UIColor {
        get {
            return UIColor(red: 0, green: 0.56, blue: 0.31, alpha: 1)
        }
    }
    
}
